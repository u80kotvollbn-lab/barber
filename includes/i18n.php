function wykryjJezyk() {
    $supported = ['en', 'pl', 'de', 'uk'];

    if (!empty($_GET['lang']) && in_array($_GET['lang'], $supported, true)) {
        $chosen = $_GET['lang'];
        setcookie('lang', $chosen, time() + (86400 * 365), '/');
        $_COOKIE['lang'] = $chosen;
        return $chosen;
    }

    if (!empty($_COOKIE['lang']) && in_array($_COOKIE['lang'], $supported, true)) {
        return $_COOKIE['lang'];
    }

    $accept = $_SERVER['HTTP_ACCEPT_LANGUAGE'] ?? '';
    if ($accept !== '') {
        foreach (explode(',', $accept) as $piece) {
            $code = strtolower(trim(explode(';', $piece)[0]));
            $primary = substr($code, 0, 2);
            if (in_array($primary, $supported, true)) {
                return $primary;
            }
        }
    }

    return 'en';
}

